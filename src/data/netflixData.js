// Helper functions to process data for visualizations
export const getGenreDistribution = () => {
  const genreCount = {};
  netflixData.forEach(item => {
    genreCount[item.genre] = (genreCount[item.genre] || 0) + 1;
  });
  return Object.entries(genreCount).map(([name, value]) => ({ name, value }));
};

export const getYearlyContent = () => {
  const yearCount = {};
  netflixData.forEach(item => {
    yearCount[item.year] = (yearCount[item.year] || 0) + 1;
  });
  return Object.entries(yearCount)
    .map(([year, count]) => ({ year: parseInt(year), count }))
    .sort((a, b) => a.year - b.year);
};

export const getTypeDistribution = () => {
  const typeCount = {};
  netflixData.forEach(item => {
    typeCount[item.type] = (typeCount[item.type] || 0) + 1;
  });
  return Object.entries(typeCount).map(([name, value]) => ({ name, value }));
};

export const getTopRated = (limit = 10) => {
  return [...netflixData]
    .sort((a, b) => b.rating - a.rating)
    .slice(0, limit);
};

export const getCountryDistribution = () => {
  const countryCount = {};
  netflixData.forEach(item => {
    countryCount[item.country] = (countryCount[item.country] || 0) + 1;
  });
  return Object.entries(countryCount)
    .map(([name, value]) => ({ name, value }))
    .sort((a, b) => b.value - a.value);
};

export const getRatingDistribution = () => {
  const ranges = {
    "9.0+": 0,
    "8.0-8.9": 0,
    "7.0-7.9": 0,
    "6.0-6.9": 0,
    "Below 6.0": 0
  };
  
  netflixData.forEach(item => {
    if (item.rating >= 9.0) ranges["9.0+"]++;
    else if (item.rating >= 8.0) ranges["8.0-8.9"]++;
    else if (item.rating >= 7.0) ranges["7.0-7.9"]++;
    else if (item.rating >= 6.0) ranges["6.0-6.9"]++;
    else ranges["Below 6.0"]++;
  });
  
  return Object.entries(ranges).map(([name, value]) => ({ name, value }));
};

